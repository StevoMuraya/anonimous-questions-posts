     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'question' => 'required',
        ]);

        question::create([
            'question' => $request->question,
        ]);

        Session::flash('message', "Your question was submitted successfully");
        return Redirect::back();
    }

